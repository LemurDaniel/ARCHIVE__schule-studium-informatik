package aufgabe3;

public class Aufgabe3 {

	public static void main(String[] args) {
		
		int i = 0;
		System.out.println("i ---> " + i);
		i = i + 1;
		System.out.println("i = i + 1 ---> " + i);
		i += 1;
		System.out.println("i += 1 ---> " + i);
		i++;
		System.out.println("i++ ---> " + i);
		System.out.println("++i ---> " + ++i);
	}

}
