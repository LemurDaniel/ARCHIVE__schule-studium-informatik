package teil1;

public class Aufgabe3 {

	public static void main(String[] args) {
		

		int i = Integer.MIN_VALUE;
		System.out.println("Minimalwert des int-Datentypes:  " + i + " - 1 = " + --i);
		System.out.println("Maximalwert des int-Datentypes:  " + i + " + 1 = " + ++i);
		
		System.out.println();
		
		long l = Long.MIN_VALUE;
		System.out.println("Minimalwert des long-Datentypes:  " + l + " - 1 = " + --l);
		System.out.println("Maximalwert des long-Datentypes:  " + l + " + 1 = " + ++l );

	}

}
